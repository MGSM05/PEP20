# Escribe un programa que use la función print() para 
#  Escribir un el número 34 en binario, otro en octal y otro en hexadecimal.
#  Escribir los números 0b1001, 0o12,  0xa3f en decimal

a = 34

print("34 en BINARIO es : " , bin(a))
print("34 en OCTAL es : " , oct(a))
print("34 en HEXADECIMAL es : " , hex(a) , "\n")

print("0b1001 en DECIMAL es : " , 0b1001)
print("0o12 en DECIMAL es : " , 0o12)
print("0xa3f en DECIMAL es : " , 0xa3f)